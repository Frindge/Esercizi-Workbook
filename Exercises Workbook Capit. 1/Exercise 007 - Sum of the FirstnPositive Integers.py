# Scrivi un programma che legga un numero intero positivo, n , dall'utente e poi visualizzi la somma di tutti
# gli interi da 1 a n. La somma dei primi n interi positivi può essere calcolata usando la formula:
# somma=(n)(n+1)/2

n=int(input("inserisci un intero positivo: "))

somma=n*(n+1)/2

print("La somma del primo", n, "intero positivo è", somma)





